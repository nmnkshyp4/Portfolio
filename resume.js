let menuicon = document.getElementsByClassName("menuicon")[0];
let menu = document.getElementsByClassName("menu")[0];
let x = document.getElementsByClassName("x")[0];

menuicon.addEventListener("click", () => {
    menu.style.visibility = "visible";
    menuicon.style.visibility = "hidden";
    x.style.visibility = "visible";
})

x.addEventListener("click", () => {
    menu.style.visibility = "hidden";
    menuicon.style.visibility = "visible";
    x.style.visibility = "hidden";
})

let work1 = document.getElementsByClassName("work1")[0];

work1.addEventListener("mouseover", () => {
    work1.style.filter = "blur(3.5px)";
    work1.style.transform = "scale(1.02)";
    work1.style.transitionDelay = "0.2s";
})
work1.addEventListener("mouseout", () => {
    work1.style.filter = "blur(0px)";
    work1.style.transform = "scale(1)";
})

